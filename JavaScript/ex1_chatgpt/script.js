/* Escreva um programa que imprima os números de 1 a 10 usando um loop do-while. */



// Inicializa a variável 'i' com o valor 1
let i = 1;

// Inicia um loop 'do-while'
do {
  // Imprime o valor atual da variável 'i' no console
  console.log(i);
  
  // Incrementa a variável 'i' em 1
  i++;
  
// Continua o loop enquanto 'i' for menor ou igual a 10
} while (i <= 10);