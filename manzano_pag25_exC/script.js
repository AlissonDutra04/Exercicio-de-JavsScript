//c) Calcular e apresentar o valor do volume de uma lata de óleo, utilizando a fórmula:
//Volume *Raio * Altura 

var raio, altura, volume;


raio = prompt("Digite o raio da lata de óleo em centímetros:");


altura = prompt("Digite a altura da lata de óleo em centímetros:");

volume = 3.14 * raio * raio * altura;

alert("O volume da lata de óleo é: " + volume + " cm³");


